export function resolveTheme(
  preset: ThemePresetId,
  customTheme?: ITheme,
): ITheme {
  if (preset === 'custom' && customTheme) {
    return customTheme;
  }
  if (preset !== 'custom') {
    return THEME_PRESETS[preset];
  }
  return THEME_PRESETS['chromeos-dark'];
}
